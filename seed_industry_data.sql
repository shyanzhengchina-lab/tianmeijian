SELECT '=== 数据验证 ===' AS info;
SELECT '班组数量' AS item, COUNT(*) AS cnt FROM base_team WHERE deleted=0
UNION ALL
SELECT '员工数量', COUNT(*) FROM base_employee WHERE deleted=0
UNION ALL
SELECT 'QC方案数量', COUNT(*) FROM qms_qc_scheme WHERE deleted=0
UNION ALL
SELECT 'QC方案明细数量', COUNT(*) FROM qms_qc_scheme_detail
UNION ALL
SELECT '质检项目数量', COUNT(*) FROM qms_inspection_item WHERE deleted=0;

SELECT '=== 方案明细统计 ===' AS info;
SELECT s.scheme_code, s.scheme_name,
       CASE s.check_type WHEN 1 THEN 'IQC' WHEN 2 THEN 'IPQC' WHEN 3 THEN 'FQC' WHEN 4 THEN '在线' WHEN 5 THEN '清洁' END AS check_type_label,
       COUNT(d.id) AS detail_count
FROM qms_qc_scheme s
LEFT JOIN qms_qc_scheme_detail d ON d.scheme_id = s.id
WHERE s.deleted=0
GROUP BY s.id
ORDER BY s.check_type, s.id;

SELECT '=== 员工岗位分布 ===' AS info;
SELECT position, COUNT(*) AS cnt FROM base_employee WHERE deleted=0 GROUP BY position ORDER BY cnt DESC;
